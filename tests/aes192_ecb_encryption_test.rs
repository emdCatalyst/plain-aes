#[test]
fn encrypt_aes192_ecb_invalid_target_size() {
    let key: &[u8] = &[
        0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f,
        0x3c,
    ];
    let plain = "abc";
    let result = encrypt(plain, CipherVersion::Aes192(key, ModeOfOperation::ECB));
    assert_eq!(result.unwrap_err(), OperationError::InvalidTargetSize);
}
